    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        if (identity.getText().equals("Resident")){
            residentInterface resident_interface = new residentInterface(ID);
            resident_interface.show();
            dispose();
            
        }
        
        else if(identity.getText().equals("Staff")){
            staffInterface staff_interface = new staffInterface(ID);
            staff_interface.show();
            dispose();
        }
        
        else if(identity.getText().equals("Manager")){
            ManagerScreen manager_interface = new ManagerScreen(ID);
            manager_interface.show();
            dispose();
        }
        
        else{
            AssistManagerScreen assistmanager_interface = new AssistManagerScreen(ID);
            assistmanager_interface.show();
            dispose();
        }
            
    }//GEN-LAST:event_backButtonActionPerformed

