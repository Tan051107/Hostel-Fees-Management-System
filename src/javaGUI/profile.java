    public profile(String user_ID) throws IOException{
        initComponents();
        this.setResizable(false);
        this.setSize(600,600);
        txtToArray approvalList = new txtToArray ();
        String [][] approvalArray = approvalList.array("ApprovalList.txt");
        System.out.println(Arrays.deepToString(approvalArray));
        displayUserProfile residentProfile = new displayUserProfile();
        int residentIndex = residentProfile.profileIndex(user_ID);
        ID = user_ID;
        System.out.println(ID);
        System.out.println(residentIndex);
        String [] userProfile = approvalArray[residentIndex];
        System.out.println(Arrays.toString(userProfile));
        userName.setText(userProfile[0]);
        userID.setText(userProfile[1]);
        identity.setText(userProfile[2]);
        birthDate.setText(userProfile[4]);
        email.setText(userProfile[5]);
        phone.setText(userProfile[6]);
        roomType.setVisible(false);
        roomTypeLabel.setVisible(false);
        if (identity.getText().equals("Resident")){
            txtToArray roomTypeList = new txtToArray ();
            String [][] roomTypeArray = roomTypeList.array("RoomTypeList.txt");
            for (int i=1;i<roomTypeArray.length;i++){
                if (roomTypeArray[i][0].equals(ID)){
                   roomType.setText(roomTypeArray[i][1]);
                }
            }
            
            roomType.setVisible(true);
            roomTypeLabel.setVisible(true);
        }



        
        
    }
    
    
    

